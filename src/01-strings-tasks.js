/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
  let str = '┌';
  for (let w = 0; w < (width - 2); w += 1) {
    str += '─';
  }
  str += '┐\n';

  for (let h = 0; h < (height - 2); h += 1) {
    str += '│';
    for (let w = 0; w < (width - 2); w += 1) {
      str += ' ';
    }
    str += '│\n';
  }
  str += '└';
  for (let w = 0; w < (width - 2); w += 1) {
    str += '─';
  }
  str += '┘\n';
  return str;
}


